# Write a python program that asks the user for their firstname, lastname and age also. Then print it out on the console in a nice format.
# hint: use input() function

# Solution
firstName = input("Please Enter Your First Name: ")
lastName = input("Enter Your Last Name: ")
age = input("Your Age Too: ")

print(f"Hello, My name is {firstName} {lastName} and I am {age} years old.")