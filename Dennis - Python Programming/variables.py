# Rules for naming variables
# 1. A variable name can have numbers but can never start with one. e.g number1, 1number

# numberOne = 1
# num20 = 20
# 2num = 21

# 2. Variable names are case-sensitive -> name Name
# name = "Dennis"
# print(name)

# 3. Variable names cannot have keywords as their names
# def = "Dennis"
# while = "loop"
# name = "Dennis"

# 4. A variable can start with an underscore '_' but cannot start with another symbol
# _name = "Dennis"
# @name = "Dennis"
# &name = "Dennis"

# 5. A variable should be descriptive ->
# userImage,
# userAge

# Multiple Assignment
a = b = c = 10
print(a)
print(b)
print(c)

# You can assign a different value for each variable within the same line of code
x, y, z = 10, 20, 30
print(x)
print(y)
print(z)

